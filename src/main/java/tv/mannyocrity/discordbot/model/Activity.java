package tv.mannyocrity.discordbot.model;

/**
 * Activity represents what the streamer is doing on a day.
 */
public enum Activity {
    /**
     * Streamer is Streaming this day.
     */
    STREAMING,
    /**
     * Streamer is support this day.
     */
    SUPPORTING,
    /**
     * Streamer is OFF this day.
     */
    OFF
}
